package estruturadados;

public class For {

	public static void main(String[] args) {
		
		System.out.println("\nImprimindo de forma crescente: \n");
		for (int i = 0; i < 10; i++) {
			System.out.println("Número " +i);
		}		
		
		System.out.println("\nImprimindo de forma decrescente: \n");
		for (int i = 10; i >= 0; i--) {
			System.out.println("Número " +i);
		}
	}
}
